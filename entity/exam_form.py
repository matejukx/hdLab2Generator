class ExamForm:

    def __init__(self,
                 student,
                 exam_type,
                 score,
                 attempt_number,
                 exam_date,
                 city):
        self.student = student
        self.exam_type = exam_type
        self.score = score
        self.attempt_number = attempt_number
        self.exam_date = exam_date
        self.city = city
